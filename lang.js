function toggleLangMenu() {
    if (window.clickSound) { window.clickSound.currentTime = 0; window.clickSound.play().catch(e => {}); }
    const langDropdown = document.getElementById('langDropdown');
    if (langDropdown) {
        langDropdown.classList.toggle('hidden');
        langDropdown.classList.toggle('flex');
    }
}

function selectLang(code, flag) {
    if (window.clickSound) { window.clickSound.currentTime = 0; window.clickSound.play().catch(e => {}); }
    
    const activeCodeEl = document.getElementById('activeCode');
    const activeFlagEl = document.getElementById('activeFlag');
    const langDropdown = document.getElementById('langDropdown');

    if (activeCodeEl) activeCodeEl.innerText = code;
    if (activeFlagEl) activeFlagEl.innerText = flag;
    if (langDropdown) {
        langDropdown.classList.add('hidden');
        langDropdown.classList.remove('flex');
    }

    if (window.dict && window.dict[code]) {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (window.dict[code][key]) {
                if (el.innerHTML.includes('<span')) {
                    const spanPart = el.innerHTML.substring(el.innerHTML.indexOf('<span'));
                    el.innerHTML = window.dict[code][key] + " " + spanPart;
                } else {
                    el.innerText = window.dict[code][key];
                }
            }
        });
        
        document.querySelectorAll('[data-i18n-ph]').forEach(el => {
            const key = el.getAttribute('data-i18n-ph');
            if (window.dict[code][key]) el.placeholder = window.dict[code][key];
        });
        
        const dashTitle = document.getElementById('dashboardTitle');
        if (dashTitle) {
            const currentTitleRaw = dashTitle.getAttribute('data-raw-title');
            if (currentTitleRaw && window.dict[code][currentTitleRaw.toLowerCase()]) {
                dashTitle.innerText = window.dict[code][currentTitleRaw.toLowerCase()];
            } else if (currentTitleRaw && (currentTitleRaw.toLowerCase() === 'plan' || currentTitleRaw.toLowerCase() === 'resize')) {
                 dashTitle.innerText = window.dict[code]['planResize'] || 'PLAN BOYUTLANDIRMA';
            }
        }
    }

    if (window._activeDashPage === 'billing' && typeof window.showBillingPage === 'function') {
        window.showBillingPage();
    }
    if (typeof applyVendorTranslations === 'function') applyVendorTranslations();
    if (typeof loadVendors === 'function') loadVendors();
}